    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        EmitirBoleto emitirBoleto = new EmitirBoleto();

        if (boxBradesco.isSelected()) {
            emitirBoleto.Bradeco(campoNome.getText(), campoDocumento.getText(), campoEndereco.getText(), campoBairro.getText(),
                    campoCep.getText(), CampoCidade.getText(), campoUF.getSelectedItem().toString(), campoData.getDate(), campoValor.getText());
            textoStatus.setText("Bradesco Emitido");
        }
        if (boxItau.isSelected()) {
            emitirBoleto.Itau(campoNome.getText(), campoDocumento.getText(), campoEndereco.getText(), campoBairro.getText(),
                    campoCep.getText(), CampoCidade.getText(), campoUF.getSelectedItem().toString(), campoData.getDate(), campoValor.getText());
            textoStatus.setText("Itau Emitido");
        }
        if (boxBrasil.isSelected()) {
            emitirBoleto.BancoDoBrasil(campoNome.getText(), campoDocumento.getText(), campoEndereco.getText(), campoBairro.getText(),
                    campoCep.getText(), CampoCidade.getText(), campoUF.getSelectedItem().toString(), campoData.getDate(), campoValor.getText());
            textoStatus.setText("Banco do Brasil Emitido");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

